package level5;

import java.util.Scanner;

public class Problem10998 {

	// 10998. A X B
	public static void main(String[] args) {

		// Scanner 생성
		Scanner scan = new Scanner(System.in);

		// 곱할 값 입력
		int A = scan.nextInt();
		int B = scan.nextInt();

		// 결과 출력
		System.out.println(A * B);

		// Scanner 종료
		scan.close();

	}

}
